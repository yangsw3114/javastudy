package prob01;

public class Printer {
	public void println(int line) {
		System.out.println(line);
	}
	public void println(boolean line) {
		System.out.println(line);
	}
	public void println(double line) {
		System.out.println(line);
	}
	public void println(String line) {
		System.out.println(line);
	}
}
